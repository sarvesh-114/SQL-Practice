CREATE DATABASE YouTube;

SET NOCOUNT ON;

CREATE TABLE creators (
  creator_id INT PRIMARY KEY,
  creator_name NVARCHAR(100),
  channel_name NVARCHAR(100),
  country NVARCHAR(50),
  join_date DATE
);

CREATE TABLE videos (
  video_id INT PRIMARY KEY,
  creator_id INT,
  title NVARCHAR(255),
  publish_date DATE,
  duration_seconds INT,
  category NVARCHAR(50)
);

CREATE TABLE daily_views (
  view_id INT PRIMARY KEY,
  video_id INT,
  view_date DATE,
  views INT,
  watch_time_seconds BIGINT,
  avg_view_duration_seconds FLOAT,
  impressions INT,
  clicks INT
);

CREATE TABLE comments (
  comment_id INT PRIMARY KEY,
  video_id INT,
  commenter_name NVARCHAR(100),
  comment_date DATE,
  sentiment NVARCHAR(20)
);

CREATE TABLE likes_dislikes (
  ld_id INT PRIMARY KEY,
  video_id INT,
  record_date DATE,
  likes INT,
  dislikes INT
);

CREATE TABLE revenue (
  revenue_id INT PRIMARY KEY,
  video_id INT,
  revenue_date DATE,
  ad_revenue DECIMAL(10,2),
  subscription_revenue DECIMAL(10,2),
  other_revenue DECIMAL(10,2)
);



